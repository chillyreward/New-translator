// Get all unique categories
export const getCategories = () => [...new Set(kikuyuPhrases.map(p => p.category))];

// Search across all fields
export const searchPhrases = (query) => {
  const q = query.toLowerCase().trim();
  return kikuyuPhrases.filter(p =>
    p.english.toLowerCase().includes(q) ||
    p.kikuyu.toLowerCase().includes(q) ||
    p.swahili.toLowerCase().includes(q) ||
    p.phonetic.toLowerCase().includes(q)
  );
};

// Get phrases by category
export const getByCategory = (category) => kikuyuPhrases.filter(p => p.category === category);

// Lookup exact English match
export const lookupEnglish = (text) => {
  const q = text.toLowerCase().trim();
  return kikuyuPhrases.find(p => p.english.toLowerCase() === q) || null;
};

// Simple word-level translation fallback
export const translateWords = (text) => {
  const words = text.toLowerCase().trim().split(/\s+/);
  return words.map(word => {
    const match = kikuyuPhrases.find(p => p.english.toLowerCase() === word);
    return match ? match.kikuyu : word;
  }).join(' ');
};

// Phonetic conversion engine for better TTS pronunciation
export function phoneticConvert(text) {
  return text
    // vowels
    .replace(/ĩ/g, 'ee')
    .replace(/ũ/g, 'oo')
    // consonant tuning
    .replace(/mw/g, 'mwe')
    .replace(/ng'/g, 'ng')
    .replace(/ny/g, 'ni')
    .replace(/th/g, 'th') // keep but emphasize
    // smoothing
    .replace(/aa/g, 'a')
    .replace(/ee/g, 'e')
    .replace(/oo/g, 'o')
    // spacing fix
    .replace(/\s+/g, ' ')
    .trim();
}

